package main

import (
	"os/exec"
	"testing"
)

func TestBuildCLI(t *testing.T) {
	cmd := exec.Command("go", "build")
	cmd.Dir = "../"
	err := cmd.Run()
	if err != nil {
		t.Fatalf("Failed to build CLI: %v", err)
	}

	cleanupCmd := exec.Command("rm", "-f", "vaultify")
	cleanupCmd.Dir = "../"
	cleanupErr := cleanupCmd.Run()
	if cleanupErr != nil {
		t.Fatalf("Failed to clean up binary: %v", cleanupErr)
	}
}
